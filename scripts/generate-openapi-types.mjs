#!/usr/bin/env node

import { mkdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const specPath = path.join(repoRoot, 'server', 'docs', 'openapi.json');
const outputPath = path.join(repoRoot, 'tdesign-vue-go', 'src', 'api', 'generated', 'schema.d.ts');

const spec = JSON.parse(await readFile(specPath, 'utf8'));

function schemaNameFromRef(ref) {
  return ref.split('/').at(-1);
}

function propertyKey(name) {
  return /^[A-Za-z_$][\w$]*$/.test(name) ? name : JSON.stringify(name);
}

function schemaToTs(schema = {}) {
  if (schema.$ref) {
    return `components["schemas"]["${schemaNameFromRef(schema.$ref)}"]`;
  }
  if (schema.enum?.length) {
    return schema.enum.map((item) => JSON.stringify(item)).join(' | ');
  }
  if (schema.type === 'array') {
    return `${schemaToTs(schema.items)}[]`;
  }
  if (schema.type === 'integer' || schema.type === 'number') {
    return 'number';
  }
  if (schema.type === 'boolean') {
    return 'boolean';
  }
  if (schema.type === 'string') {
    return 'string';
  }
  if (schema.additionalProperties) {
    const valueType = schema.additionalProperties === true ? 'unknown' : schemaToTs(schema.additionalProperties);
    return `Record<string, ${valueType}>`;
  }
  if (schema.type === 'object' && schema.properties) {
    const required = new Set(schema.required || []);
    const fields = Object.entries(schema.properties).map(([name, property]) => {
      const optional = required.has(name) ? '' : '?';
      return `${propertyKey(name)}${optional}: ${schemaToTs(property)};`;
    });
    return `{ ${fields.join(' ')} }`;
  }
  if (schema.type === 'object') {
    return 'Record<string, never>';
  }
  return 'unknown';
}

function writeSchemaObject(lines, name, schema) {
  if (schema.$ref || schema.type !== 'object' || !schema.properties) {
    lines.push(`    ${propertyKey(name)}: ${schemaToTs(schema)};`);
    return;
  }

  const required = new Set(schema.required || []);
  lines.push(`    ${propertyKey(name)}: {`);
  for (const [propertyName, property] of Object.entries(schema.properties)) {
    const optional = required.has(propertyName) ? '' : '?';
    lines.push(`      ${propertyKey(propertyName)}${optional}: ${schemaToTs(property)};`);
  }
  if (schema.additionalProperties) {
    const valueType = schema.additionalProperties === true ? 'unknown' : schemaToTs(schema.additionalProperties);
    lines.push(`      [key: string]: ${valueType};`);
  }
  lines.push('    };');
}

const lines = [
  '/* eslint-disable */',
  '// This file is generated by npm run api:types. Do not edit it by hand.',
  '',
  'export interface paths {',
];

for (const [routePath, methods] of Object.entries(spec.paths || {})) {
  lines.push(`  ${JSON.stringify(routePath)}: {`);
  for (const [method, operation] of Object.entries(methods || {})) {
    lines.push(`    ${method}: operations[${JSON.stringify(operation.operationId)}];`);
  }
  lines.push('  };');
}

lines.push('}', '');
lines.push('export interface operations {');

for (const methods of Object.values(spec.paths || {})) {
  for (const operation of Object.values(methods || {})) {
    lines.push(`  ${JSON.stringify(operation.operationId)}: {`);
    if (operation.parameters?.length) {
      lines.push('    parameters: {');
      const pathParams = operation.parameters.filter((item) => item.in === 'path');
      if (pathParams.length) {
        lines.push('      path: {');
        for (const param of pathParams) {
          lines.push(`        ${propertyKey(param.name)}: ${schemaToTs(param.schema)};`);
        }
        lines.push('      };');
      }
      const queryParams = operation.parameters.filter((item) => item.in === 'query');
      if (queryParams.length) {
        lines.push('      query: {');
        for (const param of queryParams) {
          const optional = param.required ? '' : '?';
          lines.push(`        ${propertyKey(param.name)}${optional}: ${schemaToTs(param.schema)};`);
        }
        lines.push('      };');
      }
      lines.push('    };');
    }
    if (operation.requestBody) {
      lines.push('    requestBody: {');
      lines.push('      content: {');
      for (const [contentType, mediaType] of Object.entries(operation.requestBody.content || {})) {
        lines.push(`        ${JSON.stringify(contentType)}: ${schemaToTs(mediaType.schema)};`);
      }
      lines.push('      };');
      lines.push('    };');
    }
    if (operation.security !== undefined) {
      lines.push(`    security: ${JSON.stringify(operation.security)};`);
    }
    lines.push('    responses: {');
    for (const [status, response] of Object.entries(operation.responses || {})) {
      lines.push(`      ${JSON.stringify(status)}: {`);
      if (Object.keys(response.content || {}).length) {
        lines.push('        content: {');
        for (const [contentType, mediaType] of Object.entries(response.content || {})) {
          lines.push(`          ${JSON.stringify(contentType)}: ${schemaToTs(mediaType.schema)};`);
        }
        lines.push('        };');
      }
      lines.push('      };');
    }
    lines.push('    };');
    lines.push('  };');
  }
}

lines.push('}', '');
lines.push('export interface components {');
lines.push('  schemas: {');
for (const [name, schema] of Object.entries(spec.components?.schemas || {})) {
  writeSchemaObject(lines, name, schema);
}
lines.push('  };');
lines.push('  securitySchemes: {');
lines.push('    BearerAuth: {');
lines.push('      type: "http";');
lines.push('      scheme: "bearer";');
lines.push('      bearerFormat: "JWT";');
lines.push('    };');
lines.push('  };');
lines.push('}');

await mkdir(path.dirname(outputPath), { recursive: true });
await writeFile(outputPath, `${lines.join('\n')}\n`, 'utf8');
console.log(`Generated ${path.relative(repoRoot, outputPath)}`);
