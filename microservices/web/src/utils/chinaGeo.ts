// 省名归一化：剥掉行政区划后缀取短名（新疆维吾尔自治区 → 新疆）
const PROVINCE_SUFFIXES = ['维吾尔自治区', '回族自治区', '壮族自治区', '特别行政区', '自治区', '省', '市']

export function normalizeProvince(name: string): string {
  for (const suffix of PROVINCE_SUFFIXES) {
    if (name.endsWith(suffix) && name.length > suffix.length) {
      return name.slice(0, name.length - suffix.length)
    }
  }
  return name
}

// 省名 → 归一化短名的统一入口：中文剥后缀，英文（ipinfo 兜底串）走别名表。
// 版图染色的 key 必须与 geojson 短名一致，调用方一律走这里，别单独 normalize。
export function resolveProvinceShort(name: string): string {
  return PROVINCE_EN_ALIAS[name] ?? normalizeProvince(name)
}

// 按 省/市 尽力解析落点：市级精确 → 省级锚点 → 英文省名别名 → 国家级
// （国外 IP，含中英文国名）→ null。abroad 标记供地图区分样式。
export function resolveGeoPoint(province: string, city: string): (GeoPoint & { abroad?: boolean }) | null {
  if (city) {
    const hit = CITY_COORDS[city]
    if (hit) return { name: city, lng: hit[0], lat: hit[1] }
  }
  if (province) {
    const short = normalizeProvince(province)
    const hit = PROVINCE_COORDS[short]
    if (hit) return { name: short, lng: hit[0], lat: hit[1] }
    const en = PROVINCE_EN_ALIAS[province]
    if (en) {
      const enHit = PROVINCE_COORDS[en]
      if (enHit) return { name: en, lng: enHit[0], lat: enHit[1] }
    }
    const country = COUNTRY_COORDS[province] ? province : COUNTRY_EN_ALIAS[province]
    if (country) {
      const cHit = COUNTRY_COORDS[country]
      if (cHit) return { name: country, lng: cHit[0], lat: cHit[1], abroad: true }
    }
  }
  return null
}
