('租户套餐新建', 'system:tenant-package:create', 2, '/api/v1/tenant-packages', 'POST', 2, NOW(), NOW()),
('租户套餐更新', 'system:tenant-package:update', 2, '/api/v1/tenant-packages/:id', 'PUT', 2, NOW(), NOW()),
('租户套餐删除', 'system:tenant-package:delete', 2, '/api/v1/tenant-packages/:id', 'DELETE', 2, NOW(), NOW())
ON CONFLICT DO NOTHING;

